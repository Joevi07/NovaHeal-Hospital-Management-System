    private void statbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_statbActionPerformed
        // TODO add your handling code here:
       
        try{
            DefaultCategoryDataset dataset = new DefaultCategoryDataset();
         // Your DB connection method
        String sql = "SELECT De_id, COUNT(*) AS doctor_count FROM Doctor GROUP BY De_id";
        PreparedStatement ps = cn.prepareStatement(sql);
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            int deptId = rs.getInt("De_id");
            int count = rs.getInt("doctor_count");
            dataset.addValue(count, "Doctors", "Dept " + deptId);
        }

        // Step 2: Create the chart
        JFreeChart barChart = ChartFactory.createBarChart(
                "Doctors per Department",
                "Department ID",
                "Number of Doctors",
                dataset,
                PlotOrientation.VERTICAL,
                true, true, false
        );
        Color[] barColors = {
    new Color(255, 99, 132),   // red
    new Color(54, 162, 235),   // blue
    new Color(255, 206, 86),   // yellow
    new Color(75, 192, 192),   // teal
    new Color(153, 102, 255),  // purple
    new Color(255, 159, 64),   // orange
};

BarRenderer renderer = new BarRenderer() {
    @Override
    public Paint getItemPaint(int row, int column) {
        return barColors[column % barColors.length];
    }
};

CategoryPlot plot = barChart.getCategoryPlot();
plot.setRenderer(renderer);

// Optional: gridline and background customization
plot.setBackgroundPaint(Color.WHITE);
plot.setRangeGridlinePaint(Color.DARK_GRAY);
        // Step 3: Create ChartPanel
        ChartPanel chartPanel = new ChartPanel(barChart);
        chartPanel.setPreferredSize(new java.awt.Dimension(600, 400));
        chartPanel.setMouseWheelEnabled(true);

        // Step 4: Clear and update statp panel
        statp.removeAll();
        statp.setLayout(new java.awt.BorderLayout());
        statp.add(chartPanel, java.awt.BorderLayout.CENTER);
        statp.revalidate();
        statp.repaint();

        // Step 5: Show statp in mainp
        mainp.removeAll();
        mainp.add(statp);
        mainp.revalidate();
        mainp.repaint();
        }
        catch(Exception e)
        {
             JOptionPane.showMessageDialog(null, "Error displaying chart: " + e.getMessage());
        }
        
    }//GEN-LAST:event_statbActionPerformed

