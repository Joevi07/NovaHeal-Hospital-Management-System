    private void doc_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doc_insertActionPerformed
        // TODO add your handling code here:
        int doc_id = Integer.parseInt(doc_idti.getText());
        String dept_name = doc_nameti.getText();
        String doc_contact = doc_conti.getText();
        int dept_id = Integer.parseInt(dep_docidi.getText());
        String av_date = doc_datei.getText();
        String av_time = doc_timeti.getText();
        String av_status = doc_statusti.getSelectedItem().toString();
        
       SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        
        String sql1 = "insert into Doctor values(?,?,?,?)";
        String sql2 = "insert into Doctor_Schedule values(?,?,?,?)";
        try{
        java.util.Date utildate1 = sdf.parse(av_date); 
         
        java.sql.Date sqldate1 = new java.sql.Date(utildate1.getTime());
        
        PreparedStatement ps1 = cn.prepareStatement(sql1);
        PreparedStatement ps2 = cn.prepareStatement(sql2);
        ps1.setInt(1,doc_id);
        ps1.setString(2,dept_name);
        ps1.setInt(3,dept_id);
        ps1.setString(4,doc_contact);
        ps2.setInt(1,doc_id);
        ps2.setDate(2,sqldate1);
        ps2.setString(3,av_time);
        ps2.setString(4,av_status);
        int rows1 = ps1.executeUpdate();
        int rows2 = ps2.executeUpdate();
        if(rows1>0)
        {
            JOptionPane.showMessageDialog(null,"Inserted value in Doctor");
            if(rows2>0)
           {
               JOptionPane.showMessageDialog(null,"Inserted value in Doctor Schedule");
           }
            else{
            JOptionPane.showMessageDialog(null,"Insertion failed in Doctor Schedule");
            }
        }
        else{
        JOptionPane.showMessageDialog(null,"Insertion failed in Doctor");
        }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Error: "+e.getMessage());
        }
    }//GEN-LAST:event_doc_insertActionPerformed

