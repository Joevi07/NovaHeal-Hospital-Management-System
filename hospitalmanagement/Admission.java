    private void adm_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adm_insertActionPerformed
        // TODO add your handling code here:
        String sql = "insert into Admission values(?,?,?,?,?,?,?,?)";
        int adm_id = Integer.parseInt(adm_adidi.getText());
        int pa_id = Integer.parseInt(adm_paidi.getText());
        int room_id = Integer.parseInt(adm_roidi.getText());
        int rec_id = Integer.parseInt(adm_reidi.getText());
        String ad_date = adm_adai.getText();
        String dis_date = adm_disdi.getText();
        String bk_time = adm_bkti.getText();
        String ubk_time = adm_ubki.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        SimpleDateFormat timestampFormat = new SimpleDateFormat("dd-MM-yyyy HH:mm:ss");

        try{
             java.util.Date utilDate1 = sdf.parse(ad_date);
             java.util.Date utilDate2 = sdf.parse(dis_date);
            java.sql.Date sqlDate1 = new java.sql.Date(utilDate1.getTime());
             java.sql.Date sqlDate2 = new java.sql.Date(utilDate2.getTime());
             java.util.Date utilTime1 = timestampFormat.parse(bk_time);
             java.util.Date utilTime2 = timestampFormat.parse(ubk_time);
             java.sql.Timestamp sqlTime1 = new java.sql.Timestamp(utilTime1.getTime());
             java.sql.Timestamp sqlTime2 = new java.sql.Timestamp(utilTime2.getTime());
            PreparedStatement ps = cn.prepareStatement(sql);
            ps.setInt(1,adm_id);
            ps.setInt(2,pa_id);
            ps.setInt(3,room_id);
            ps.setInt(4,rec_id);
            ps.setDate(5,sqlDate1);
            ps.setDate(6,sqlDate2);
            ps.setTimestamp(7,sqlTime1);
            ps.setTimestamp(8,sqlTime2);
            int rows = ps.executeUpdate();
            if(rows>0)
            {
                JOptionPane.showMessageDialog(null, "Inserted into Admission");
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Insertion failed in Admission");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Error: "+e.getMessage());
        }
    }//GEN-LAST:event_adm_insertActionPerformed

