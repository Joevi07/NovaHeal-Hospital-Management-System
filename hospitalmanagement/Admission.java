    // Chart 1: Bar chart for number of admissions per day
    private ChartPanel createAdmissionBarChart() {
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();

        try{
            String sql = "SELECT Ad_date, COUNT(*) FROM Admission GROUP BY Ad_date ORDER BY Ad_date";
            PreparedStatement ps = cn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            SimpleDateFormat sdf = new SimpleDateFormat("dd-MM");

            while (rs.next()) {
                String date = sdf.format(rs.getDate(1));
                int count = rs.getInt(2);
                dataset.addValue(count, "Admissions", date);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error loading bar chart: " + e.getMessage());
        }

        JFreeChart chart = ChartFactory.createBarChart(
                "Admissions Per Day", "Date", "Count",
                dataset, PlotOrientation.VERTICAL,
                false, true, false
        );

        chart.setBackgroundPaint(Color.WHITE);
        CategoryPlot plot = chart.getCategoryPlot();
        plot.setBackgroundPaint(new Color(230, 245, 255));

        BarRenderer renderer = (BarRenderer) plot.getRenderer();
        for (int i = 0; i < dataset.getColumnCount(); i++) {
            renderer.setSeriesPaint(0, new Color(100, 180, 250));
        }

        return new ChartPanel(chart);
    }

    // Chart 2: Line chart for average stay duration
    private ChartPanel createStayDurationLineChart() {
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();

        try  {
          String sql = "SELECT TRUNC(Ad_date) AS Ad_date, " +
                 "AVG(TRUNC(Dis_date) - TRUNC(Ad_date)) AS avg_stay " +
                 "FROM Admission " +
                 "WHERE Dis_date IS NOT NULL " +
                 "GROUP BY TRUNC(Ad_date) " +
                 "ORDER BY TRUNC(Ad_date)";
    
    PreparedStatement ps = cn.prepareStatement(sql);
    ResultSet rs = ps.executeQuery();
    SimpleDateFormat sdf = new SimpleDateFormat("dd-MM");

    while (rs.next()) {
        String date = sdf.format(rs.getDate("Ad_date"));
        double avg = rs.getDouble("avg_stay");
        avg = Math.round(avg * 100.0) / 100.0;  // Round to 2 decimal places
        dataset.addValue(avg, "Avg Stay (Days)", date);
    }

    rs.close();
    ps.close();
}

            

         catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error loading line chart: " + e.getMessage());
        }

        JFreeChart chart = ChartFactory.createLineChart(
                "Average Stay Duration", "Date", "Days",
                dataset, PlotOrientation.VERTICAL,
                false, true, false
        );

        chart.setBackgroundPaint(Color.WHITE);
        CategoryPlot plot = chart.getCategoryPlot();
        plot.setBackgroundPaint(new Color(255, 250, 230));

        LineAndShapeRenderer renderer = new LineAndShapeRenderer();
        renderer.setSeriesPaint(0, new Color(255, 99, 71));
        plot.setRenderer(renderer);

        return new ChartPanel(chart);
    }

    // Chart 3: Pie chart for room-wise admissions
    private ChartPanel createRoomWisePieChart() {
        DefaultPieDataset dataset = new DefaultPieDataset();

        try  {
            String sql = "SELECT R_id, COUNT(*) FROM Admission GROUP BY R_id";
            PreparedStatement ps = cn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                String room = "Room " + rs.getInt(1);
                int count = rs.getInt(2);
                dataset.setValue(room, count);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error loading pie chart: " + e.getMessage());
        }

        JFreeChart chart = ChartFactory.createPieChart(
                "Admissions by Room",
                dataset, true, true, false
        );

        chart.setBackgroundPaint(Color.WHITE);
        chart.getPlot().setBackgroundPaint(new Color(240, 255, 240));

        return new ChartPanel(chart);
    }
