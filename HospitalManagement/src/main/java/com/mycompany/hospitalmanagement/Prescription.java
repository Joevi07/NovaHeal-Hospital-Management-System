     public JPanel createAllChartsPanel() {
        JPanel combinedPanel = new JPanel();
        combinedPanel.setLayout(new GridLayout(1, 3));  // 3 charts side by side

        // 1. Bar Chart Dataset
        DefaultCategoryDataset barDataset = new DefaultCategoryDataset();

        // 2. Line Chart Dataset (same or different)
        DefaultCategoryDataset lineDataset = new DefaultCategoryDataset();

        // 3. Pie Chart Dataset
        DefaultPieDataset pieDataset = new DefaultPieDataset();

        try {
            

            // Sample Query: Count of prescriptions per doctor
            String sql = "SELECT D_id, COUNT(*) AS total FROM Prescription GROUP BY D_id";
            PreparedStatement ps = cn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                String doctor = "Doctor " + rs.getInt("D_id");
                int count = rs.getInt("total");
                barDataset.addValue(count, "Prescriptions", doctor);
                lineDataset.addValue(count, "Prescriptions", doctor);
                pieDataset.setValue(doctor, count);
            }

            rs.close();
            ps.close();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error loading charts: " + e.getMessage());
        }

        // ===== BAR CHART =====
        JFreeChart barChart = ChartFactory.createBarChart(
                "Prescriptions per Doctor",
                "Doctor",
                "Count",
                barDataset,
                PlotOrientation.VERTICAL,
                false, true, false
        );
        BarRenderer barRenderer = (BarRenderer) barChart.getCategoryPlot().getRenderer();
        for (int i = 0; i < barDataset.getColumnCount(); i++) {
            barRenderer.setSeriesPaint(0, new Color(100 + i * 20, 100, 200 - i * 20));
        }

        // ===== LINE CHART =====
        JFreeChart lineChart = ChartFactory.createLineChart(
                "Prescriptions Trend per Doctor",
                "Doctor",
                "Count",
                lineDataset,
                PlotOrientation.VERTICAL,
                false, true, false
        );
        LineAndShapeRenderer lineRenderer = new LineAndShapeRenderer();
        lineChart.getCategoryPlot().setRenderer(lineRenderer);

        // ===== PIE CHART =====
        JFreeChart pieChart = ChartFactory.createPieChart(
                "Prescription Share per Doctor",
                pieDataset,
                true, true, false
        );

        // ===== Wrap in Panels =====
        ChartPanel barPanel = new ChartPanel(barChart);
        ChartPanel linePanel = new ChartPanel(lineChart);
        ChartPanel piePanel = new ChartPanel(pieChart);

        // ===== Add to Main Panel =====
        combinedPanel.add(barPanel);
        combinedPanel.add(linePanel);
        combinedPanel.add(piePanel);

        return combinedPanel;
    }
