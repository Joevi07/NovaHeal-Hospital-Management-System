    private void pre_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pre_insertActionPerformed
        // TODO add your handling code here:
        String sql = "insert into Prescription values(?,?,?,?,?,?)";
        int pres_id = Integer.parseInt(pres_preidti.getText());
        int doc_id = Integer.parseInt(pres_didti.getText());
        int med_id = Integer.parseInt(pre_midti.getText());
        String b_id = pre_bidti.getText();
        int pa_id = Integer.parseInt(pres_pidti.getText());
         int pre_fee = Integer.parseInt(pre_feei.getText());

        try{
            PreparedStatement ps = cn.prepareStatement(sql);
            ps.setInt(1,pres_id);
            ps.setInt(2,doc_id);
            ps.setInt(3,pa_id);
            ps.setInt(4,med_id);
            ps.setString(5,b_id);
            ps.setInt(6,pre_fee);
            int rows = ps.executeUpdate();
            if(rows>0)
            {
                JOptionPane.showMessageDialog(null, "Inserted into Prescription");
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Insertion failed in Prescription");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Error: "+e.getMessage());
        }

    }//GEN-LAST:event_pre_insertActionPerformed

